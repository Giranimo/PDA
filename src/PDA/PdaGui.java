    /**
     * This method changes which panels are currently showed, changes the color
     * of another panel and calls the method that creates the thread.
     *
     * @param evt
     */
    private void pdaLoginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdaLoginBtnActionPerformed
        pdaLoginPanel.setVisible(false);
        pdaLogoutPanel.setVisible(true);
        pdaColorPanel.setVisible(true);

        pdaColorPanel.setBackground(Color.BLUE);

        startThread = true;

        startUnicaster(pdaUsernameTextField.getText());
        startMulticaster(pdaUsernameTextField.getText(), pdaPasswordTextField.getText());



    }//GEN-LAST:event_pdaLoginBtnActionPerformed

    /**
     * This method changes which panels are currently shows and sets the boolean
     * value of startThread to false which ends the while loop in the thread.
     *
     * @param evt
     */
    private void pdaLogoutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pdaLogoutBtnActionPerformed
        pdaLoginPanel.setVisible(true);
        pdaLogoutPanel.setVisible(false);
        pdaColorPanel.setVisible(true);

        interruptThreads();
        startThread = false;

    }//GEN-LAST:event_pdaLogoutBtnActionPerformed

