    /**
     * This method creates and starts a thread and requires a username and
     * password
     *
     * @param username
     * @param password
     */
    public void startMulticaster(String username, String password) {
        pdaThread = new PdaMulti(this, username, password, unicasterThread);
        pdaThread.start();
    }

    public void startUnicaster(String username) {
        unicasterThread = new PdaUni(this, username);
        unicasterThread.start();

    }

    public void interruptThreads() {
        pdaThread.interrupt();
        unicasterThread.interrupt();
    }

    /**
     * This method makes the correct panel visible at the start of the program.
     */
    public void pdaStartWindow() {
        pdaLoginPanel.setVisible(true);
        pdaLogoutPanel.setVisible(false);
        pdaColorPanel.setVisible(false);
    }

